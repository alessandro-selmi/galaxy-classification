def train_and_evaluate_logreg(feature_list):
    valid_feature_aliases = [alias for alias in feature_list if alias in feature_function_map]
    if len(valid_feature_aliases) == 0:
        print(f'No valid features in list; the valid features are {list(feature_function_map.keys())}.')
        return None, None

    input_dim = sum(feature_dims.get(alias, 0) for alias in valid_feature_aliases)
    nclasses = len(full_train_set.classes)

    MODELS_DIR = os.path.join(PROJECT_DIR, "Galaxies/LogRegModels")
    model_filename = f"model__{'__'.join(valid_feature_aliases)}.pth"
    model_save_path = os.path.join(MODELS_DIR, model_filename)
    need_training = False

    if os.path.exists(model_save_path):
        print(f"Found existing model for features {valid_feature_aliases} at {model_save_path}. Loading and evaluating...")
        try:
            model = torch.nn.Linear(input_dim, nclasses)
            model.to(DEVICE)
            model.load_state_dict(torch.load(model_save_path, map_location=DEVICE))

            model.eval()
            corr_pred_val = 0
            tot_samp_val = 0

            with torch.no_grad():
                for images, labels in val_loader:
                    images = images.to(DEVICE)
                    labels = labels.to(DEVICE)

                    feats = []
                    for alias in valid_feature_aliases:
                        feature_func = feature_function_map[alias]
                        feats.append(feature_func(images))
                    features = torch.cat(feats, dim=1)

                    logits = model(features)
                    predictions = torch.argmax(logits, dim=1)
                    corr_pred_val += (predictions == labels).sum().item()
                    tot_samp_val += labels.size(0)
            best_val_acc = corr_pred_val / tot_samp_val
            print(f"Validation accuracy (loaded model): {best_val_acc * 100:.1f}%")

        except Exception as e:
            print(f"Error loading or evaluating model from {model_save_path}: {e}. Proceeding with training instead.")
            need_training = True
    else:
        need_training = True

    if need_training:
        LR = 0.1
        model = torch.nn.Linear(input_dim, len(full_train_set.classes))
        model.to(DEVICE)
        optimizer = torch.optim.Adam(model.parameters(), lr=LR)

        best_val_acc = 0
        epochs_no_improve = 0
        best_model_state = None

        print(f"Starting training with features: {valid_feature_aliases}")
        steps = 0

        for epoch in range(1, EPOCHS+1):
          corr_pred_train = 0
          tot_samp_train = 0
          model.train()

          for images, labels in train_loader:
            images = images.to(DEVICE)
            labels = labels.to(DEVICE)

            feats = []    # tensors list
            for alias in valid_feature_aliases:
                feature_func = feature_function_map[alias]
                feats.append(feature_func(images))

            features = torch.cat(feats, dim=1)
            # cannot directly call torch.cat(feature_func, dim=1) because that would try to return a cat of features' names

            optimizer.zero_grad()
            logits = model(features)
            loss = loss_fun(logits, labels)
            loss.backward()
            optimizer.step()
            steps += 1

            predictions = torch.argmax(logits, dim=1)
            corr_pred_train += (predictions == labels).sum().item()
            tot_samp_train += labels.size(0)

          train_acc = corr_pred_train / tot_samp_train

          # start of valid loop
          corr_pred_val = 0
          tot_samp_val = 0
          model.eval()
          with torch.no_grad():
              for images, labels in val_loader:
                  images = images.to(DEVICE)
                  labels = labels.to(DEVICE)

                  feats = []
                  for alias in valid_feature_aliases:
                      feature_func = feature_function_map[alias]
                      feats.append(feature_func(images))
                  features = torch.cat(feats, dim=1)

                  logits = model(features)
                  predictions = torch.argmax(logits, dim=1)
                  corr_pred_val += (predictions == labels).sum().item()
                  tot_samp_val += labels.size(0)
          val_acc = corr_pred_val / tot_samp_val

          if val_acc > best_val_acc:
            best_val_acc = val_acc
            best_model_state = copy.deepcopy(model.state_dict())
            epochs_no_improve = 0
          else:
              epochs_no_improve += 1


          print(f"Epoch {epoch}, loss {loss.item():.3f}, train acc. {train_acc * 100:.1f}%, val acc. {val_acc * 100:.1f}%")

          if epochs_no_improve == patience:
              print(f"Early stopping on epoch {epoch}.")
              break

          if epoch % 15 == 0:
            LR *= 0.1
            for param_group in optimizer.param_groups:
                param_group['lr'] = LR

        if best_model_state is not None:
            try:
                model.load_state_dict(best_model_state)
                print("Loaded best model state based on validation accuracy.")
                torch.save(best_model_state, model_save_path)
                print(f"Best model state saved to {model_save_path}")
            except Exception as e:
                print(f"Error saving model state to {model_save_path}: {e}")
        else:
            print("No best model state found (maybe due to no validation data or 0 epochs?). Using final epoch state.")

        print(f"Finished training. Evaluating on test set...")


    model.eval()
    cm = torch.zeros(nclasses, nclasses, dtype=torch.long)

    with torch.no_grad():
        for images, labels in test_loader:
            images = images.to(DEVICE)
            labels = labels.to(DEVICE)

            feats = []
            for alias in valid_feature_aliases:
                feature_func = feature_function_map[alias]
                feats.append(feature_func(images))

            features = torch.cat(feats, dim=1)

            logits = model(features)
            predictions = torch.argmax(logits, dim=1)

            for l, p in zip(labels.view(-1), predictions.view(-1)):
              cm[l, p] += 1

    cm = cm / cm.sum(1).unsqueeze(1)
    cm_np = cm.numpy()

    test_acc = cm_np.trace() / cm_np.sum()
    print(f"Test accuracy: {test_acc * 100:.1f}%")

    plt.figure(figsize=(6, 5), dpi=100)
    threshold = 5/100
    annot = np.full(cm_np.shape, '', dtype=object)
    for i in range(nclasses):
        for j in range(nclasses):
            if cm_np[i, j] > threshold:
                annot[i, j] = f"{cm_np[i, j]*100:.1f}%"

    sns.heatmap(cm_np, annot=annot, fmt='', cmap="Blues", linewidths=0.5,
            xticklabels=full_train_set.classes, yticklabels=full_train_set.classes,
            cbar=True, square=True, annot_kws={"size": 10, "color": "white"})

    plt.xlabel("Predicted Label")
    plt.ylabel("True Label")
    plt.title(f"CM for {valid_feature_aliases}", fontsize=12)
    plt.show()
    return best_val_acc, test_acc

