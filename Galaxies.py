def color_histogram(img, bins=64):
    n = img.shape[0]  # batch size (n of images in batch)
    img = (img * bins).long().clamp(0, bins - 1)  # RGB channels discretization in bins
    img = img + torch.arange(0, 3 * n * bins, bins, device=img.device).view(n, 3, 1, 1) # encoding, splitting images, channels and intensities
    hist = torch.bincount(img.view(-1), minlength=(3 * n * bins)).view(n, 3 * bins) # counting and populating histo; hist (n, 3*bins): every image has a concat R+G+B histo
    return hist / img[0].numel()  # normalization


def edge_direction_histogram(img, bins=64):
    n = img.shape[0]  # batch size (n of images in batch)
    SOBEL = torch.tensor([                          # Sobel convolution:
        [[[-1, 0, 1], [-2, 0, 2], [-1, 0, 1]]],     # high values on edges,
        [[[-1, -2, -1], [0, 0, 0], [1, 2, 1]]]      # low on flat;
        ], dtype=img.dtype, device=img.device)      # basically x and y derivatives
    gray = img.mean(1, True)  # averaging on channels, dim 1: (n, channel, h, w)
    grad = torch.nn.functional.conv2d(gray, SOBEL) # convolution on gray level, returns a (n, 2, h, w) array
    magnitude = grad.norm(dim=1)  # grad magnitude, L2 norm: sqrt(grad[0]^2 + grad[1]^2)
    angle = torch.atan2(grad[:, 0], grad[:, 1]) # grad direction: edge direction
    directions = (bins * angle / (2 * torch.pi)).long() % bins  # direction bins
    directions = directions + torch.arange(0, n * bins, bins, device=img.device).view(n, 1, 1)  # shifts batches to separate images histograms
    hist = torch.bincount(directions.view(-1), weights=magnitude.view(-1), minlength=n * bins).view(n, bins)  # builds histo by magnitude sum
    hist = hist / hist.sum(1, True).clamp(1e-6) # normalization
    return hist


def _cooccurrence_matrix_dir(values, bins, di, dj):
    b, m, n = values.shape  # batch, height, width
    codes = values[:, :m - di, :n - dj] + bins * values[:, di:, dj:]  # code = val1 + bins*val2: code for pair of pixels at distance (di, dj)
    codes += torch.arange(0, b * (bins ** 2), bins ** 2, device=values.device).view(b, 1, 1)   # batch offset to distinguish images
    entries = torch.bincount(codes.view(-1), minlength=b * (bins ** 2)).view(b, bins, bins) # builds the bins x bins matrix
    return entries


def rgb_cooccurrence_matrix(img, quantization=3, distance=10):
    n = img.shape[0]  # batch size (n of images in batch)
    img = (img * quantization).long().clamp(0, quantization - 1)  # RGB channels quantization: a little, some, a lot
    img = (img * (torch.arange(3, device=img.device) ** quantization).view(1, 3, 1, 1)).sum(1)  # single-value RGB combinations encoding
    bins = quantization ** 3  # total bin number
    mat = _cooccurrence_matrix_dir(img, bins, distance, 0)    # populating matrix by vertical distance
    mat += _cooccurrence_matrix_dir(img, bins, 0, distance)   # first and then horizontal distance
    mat = (mat + mat.permute(0, 2, 1)).view(n, -1)  # matrix symmetrizing and flattening
    mat = mat / mat.sum(1, True)  # normalization
    return mat  # returns a (n, bins^2) object


