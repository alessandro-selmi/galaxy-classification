ytr = np.asarray(train_labels)  # (n_train,) 1D
yva = np.asarray(val_labels)    # (n_val,)
yte = np.asarray(test_labels)   # (n_test,)

n_classes = 4
class_names = full_train_set.classes
bart_models = {}


# training (One-vs-All)
for cls in range(n_classes):
    print(f"Training BART for {class_names[cls].capitalize()} vs rest...")
    # binary target: 1 if current class, else 0; must be 1D of length n_train
    y_train_bin = (ytr == cls).astype(np.float32) # float because gbart is based on regression: the
                                                  # output in [0, 1] can later be interpreted as
                                                  # probability of the sample belonging to that class
    # gbart expects X with shape (p, n), y with shape (n,)
    bart_models[cls] = gbart(Xtr_bz, y_train_bin, ntree=200, printevery=200, seed=50, nskip=100, ndpost=1900, maxdepth=6)
    print("\n")


# prediction helper
def bart_ovr_predict(X):
    """
    For each sample, computes probability P(y=cls|x) for all classes
    by averaging posterior draws. Then assigns the class with max prob.
    """
    prob_matrix = []
    for cls in range(n_classes):
        # yhat shape = (ndpost, n_samples)
        yhat = bart_models[cls].predict(X)
        # takes posterior mean per sample -> shape (n_samples,)
        prob = yhat.mean(axis=0)
        prob_matrix.append(prob)

    prob_matrix = np.vstack(prob_matrix).T  # shape: (n_samples, n_classes)
    preds = np.argmax(prob_matrix, axis=1)
    return preds, prob_matrix


