def calc_val_acc_han():
  ok = 0
  tot = 0
  net.eval()
  with torch.no_grad():
      for images, labels in val_loader:
          images = images.to(DEVICE)
          labels = labels.to(DEVICE)

          color_hist_feat = color_histogram(images)
          edge_hist_feat = edge_direction_histogram(images)
          coocc_mat_feat = rgb_cooccurrence_matrix(images)

          features = torch.cat([color_hist_feat, edge_hist_feat, coocc_mat_feat], dim=1)

          logits = net(features)
          predictions = torch.argmax(logits, dim=1)
          ok += (predictions == labels).long().sum()
          tot += len(labels)
  val_acc = (ok / tot) if tot > 0 else 0.0
  net.train()
  return val_acc


def load_or_train_mlp_han(LR = 1e-2, EPOCHS = 30, WD = 0, lam = 0, patience=20,
                  overfit_tol=1, early_stopping=True, early_stop_threshold=0,
                  early_stop_epochs=5, val_acc_to_save=0, cm_title=""):
  '''
  needs net and net_path to be defined
  other parameters: LR, EPOCHS, WD, lam, patience, overfit_tol, early_stopping, early_stop_threshold,
      early_stop_epochs, val_acc_to_save, cm_title)
  '''

  to_delete = 0
  best_epoch = 0

  if os.path.exists(net_path):
    print("Loading model weights from:", net_path)

  else:
    print("Training a new model...")
    net.to(DEVICE)

    ps = sum(p.numel() for p in net.parameters())
    print(f"Number of parameters: {ps}")

    MOMENTUM = 0.9
    loss_fun = torch.nn.CrossEntropyLoss()
    optimizer = torch.optim.SGD(net.parameters(), lr=LR, momentum=MOMENTUM, weight_decay=WD)
    scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(optimizer, mode='max', factor=0.1, patience=5)
    # scheduler helps adjusting the learning rate

    best_val_acc = 0.0
    epochs_without_improvement = 0
    best_model_state = None

    steps = 0
    for epoch in range(1, EPOCHS+1):
        net.train()
        for images, labels in train_loader:
            images = images.to(DEVICE)
            labels = labels.to(DEVICE)

            color_hist_feat = color_histogram(images)
            edge_hist_feat = edge_direction_histogram(images)
            coocc_mat_feat = rgb_cooccurrence_matrix(images)

            features = torch.cat([color_hist_feat, edge_hist_feat, coocc_mat_feat], dim=1)

            optimizer.zero_grad()
            logits = net(features)
            loss = loss_fun(logits, labels) + lam * sum(p.abs().sum() for p in net.parameters()) # includes L1 penalty
            loss.backward()
            optimizer.step()
            steps += 1

        net.eval()
        train_ok = 0
        train_tot = 0
        with torch.no_grad():
            for images, labels in train_loader:
                images = images.to(DEVICE)
                labels = labels.to(DEVICE)

                color_hist_feat = color_histogram(images)
                edge_hist_feat = edge_direction_histogram(images)
                coocc_mat_feat = rgb_cooccurrence_matrix(images)

                features = torch.cat([color_hist_feat, edge_hist_feat, coocc_mat_feat], dim=1)

                logits = net(features)
                predictions = torch.argmax(logits, dim=1)
                train_ok += (predictions == labels).long().sum()
                train_tot += len(labels)
        train_acc = (train_ok / train_tot).item() if train_tot > 0 else 0.0
        net.train()

        val_acc = calc_val_acc_han()

        if epoch == 5:
          for g in optimizer.param_groups:
            g['lr'] /= 10
        #elif epoch > 5:
        scheduler.step(val_acc)

        print((f"Epoch {epoch}, step {steps}, "
               f"loss {loss.item():.4f}, "
               f"train acc. {train_acc * 100:.1f}%, "
               f"val acc. {val_acc * 100:.1f}%; "
               f"updating lr to {optimizer.param_groups[0]['lr']:.2e}"))

        # Early stopping
        if early_stopping:
          # Early stopping due to low val_acc
          if epoch == early_stop_epochs:
              if val_acc < early_stop_threshold:
                  print(f"Discarding model with validation accuracy {100*val_acc:.1f} after {epoch} epochs")
                  break
          # Early stopping due to val_acc worsen
          if val_acc > best_val_acc:  # Checks if accuracy has improved
              best_val_acc = val_acc
              best_epoch = epoch
              epochs_without_improvement = 0
              best_model_state = copy.deepcopy(net.state_dict())  # Saves best model if val_acc high enough
          else:
              epochs_without_improvement += 1
              if epochs_without_improvement >= patience:
                  print(f"Early stopping at epoch {epoch} with best validation accuracy {best_val_acc * 100:.1f}% from epoch {best_epoch}")
                  break
          # Early stopping due to overfitting
          if train_acc - val_acc > overfit_tol:
              print(f"Early stopping at epoch {epoch} due to overfitting")
              break

        if not early_stopping and val_acc > val_acc_to_save:
            best_model_state = copy.deepcopy(net.state_dict())
            best_epoch = epoch


    print("Training completed")


    if best_model_state is not None:
        try:
            os.makedirs(os.path.dirname(net_path), exist_ok=True)
            torch.save(best_model_state, net_path) # saves best_model_state
            if best_val_acc > val_acc_to_save:
                print(f"Best model state from epoch {best_epoch} saved to {net_path}. Reloading before evaluation.")
            else:
                to_delete = 1
                print("Best validation accuracy not high enough: probably not leaving any model state after evaluation.")
        except Exception as e:
            print(f"Error saving best model state to {net_path}: {e}")
    else:
        print("No best model state found or saved. Proceeding with the final state.")


  net.load_state_dict(torch.load(net_path, map_location=lambda storage, loc: storage))
  net.to(DEVICE)
  final_val_acc = calc_val_acc_han()
  print(f"Validation accuracy: {final_val_acc * 100:.1f}%")

  if to_delete == 1:
    if final_val_acc < val_acc_to_save:
        print(f"Deleting model state.")
        os.remove(net_path)
    else:
        print(f"Final validation accuracy high enough: keeping model state at {net_path}.")


  print(f"Evaluating on test set...")
  net.eval()
  # confusion matrix
  cm = torch.zeros(nclasses, nclasses, dtype=torch.long)
  with torch.no_grad():
      for images, labels in test_loader:
          images = images.to(DEVICE)
          labels = labels.to(DEVICE)

          color_hist_feat = color_histogram(images)
          edge_hist_feat = edge_direction_histogram(images)
          coocc_mat_feat = rgb_cooccurrence_matrix(images)

          features = torch.cat([color_hist_feat, edge_hist_feat, coocc_mat_feat], dim=1)

          logits = net(features)
          predictions = torch.argmax(logits, dim=1)

          for i in range(len(labels)):
              cm[labels[i], predictions[i]] += 1

  test_acc = cm.trace().item() / cm.sum().item() if cm.sum() > 0 else 0.0
  print(f"Test accuracy: {test_acc * 100:.1f}%")

  cm = cm / cm.sum(1).unsqueeze(1)  # Normalization
  cm_np = cm.numpy()

  plt.figure(figsize=(6, 5), dpi=100)
  threshold = 5/100
  annot = np.full(cm_np.shape, '', dtype=object)
  for i in range(nclasses):
      for j in range(nclasses):
          if cm_np[i, j] > threshold:
              annot[i, j] = f"{cm_np[i, j]*100:.1f}%"

  sns.heatmap(cm_np, annot=annot, fmt='', cmap="Blues", linewidths=0.5,
          xticklabels=full_train_set.classes, yticklabels=full_train_set.classes, cbar=True,
          square=True, annot_kws={"size": 10, "color": "white"})

  plt.xlabel("Predicted Label")
  plt.ylabel("True Label")
  plt.title(f"Confusion Matrix {cm_title}", fontsize=12)
  plt.show()



  return best_val_acc, test_acc

